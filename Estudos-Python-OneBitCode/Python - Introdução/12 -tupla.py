gamesTuple = ('Fifa 23' , 'red dead 2 ' , 'Star wars' , 'Mario World' , 'Resdient evil 4')
print(gamesTuple)
#Não podemos Adicionar valores,remover e ordenar valores

#1- Primeiuro item da lista
print(gamesTuple[:2])

#2 - ultimo item da lista
print(gamesTuple[-1])

#3 - Buscar jogos até uma posição
print(gamesTuple[:3])

#4- Buscar jogos de uma posição
print(gamesTuple[2:])

#5- Recuperar item pelo inidice
print(gamesTuple.index('Star wars'))