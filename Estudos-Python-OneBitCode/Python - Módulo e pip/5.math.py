import math
#1 Pi]
print(math.pi)
print(f'{math.pi:.2f}')


#2 - acessar de euler
print(math.e)
print(f'{math.e:.2f}')

#3 - Arrendondar cima ou baixo
num1 = 10.4
print(math.ceil(num1))
print(math.floor(num1))

#4 - fatorial de um número
num2 = int(input('Digite um número:'))
print(math.factorial(num2))

#5 - potência de um número!
print(math.pow(5,5))

#6 - Raiz Quadrada
print(math.sqrt(169))


#7 - MDC
mdc = math.gcd(8,10)
print(mdc)

#8 - logáritimo
print(math.log(10))